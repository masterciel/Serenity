using System.Runtime.CompilerServices;

namespace google.maps
{
    [Imported]
    public enum Animation
    {
        /// <summary>
        /// Marker bounces until animation is stopped.
        /// </summary>
        Bounce = 1,
        /// <summary>
        /// Marker falls from the top of the map ending with a small bounce.
        /// </summary>
        Drop = 2
    }
}